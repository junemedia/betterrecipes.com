class Nitro {
  var $apiKey;
  var $secretKey;
  var $baseURL;
  var $userId;
  var $sessionKey;
  var $lastRequest;
  var $lastResponse;
  var $lastResponseParsed;
  public function  __construct($apiKey = 'bf356071a5044de680cd39caab431708', $secretKey = '997df03fd8f5459795a767b2c455b753', $baseURL = 'https://meredithsweeps.nitro.bunchball.net/nitro/json') {
    $this->apiKey    = $apiKey;
    $this->secretKey = $secretKey;
    $this->baseURL   = rtrim($baseURL, '/?');
  }
  public function userLogin($userId, $firstName = null, $lastName = null) {
    $this->userId = $userId;
    $signature = $this->getSignature();
    $response = $this->doRequest('user.login', array(
      'apiKey'    => $this->apiKey,
      'userId'    => $this->userId,
      'ts'        => $this->time,
      'sig'       => $signature,
      'firstName' => $firstName,
      'lastName'  => $lastName
      ));
    $this->sessionKey = $response->Login->sessionKey;
    return $this->sessionKey;
  }
  public function getSignature() {
    $this->time = time();
    $unencryptedSignature = $this->apiKey.$this->secretKey.$this->time.$this->userId;
    $length = strlen($unencryptedSignature);
    $unencryptedSignature = $unencryptedSignature.$length;
    $signature = md5($unencryptedSignature);
    return $signature;
  }
  public function doRequest($method, array $params = array()) {
    $request = $this->baseURL.'?method='.$method;
    foreach ($params as $k => $v) {
      $request .= '&'.$k.'='.$v;
    }
    $this->lastRequest = $request;
    $response = file_get_contents($request);
    $this->lastResponse = utf8_encode($response);
    $this->lastResponseParsed = json_decode($this->lastResponse);
    return $this->lastResponseParsed->Nitro;
  }
}
